package javatutorial;

public class MultiplicationTables {

    public static void main(String[] args) {
        for (int n = 1; n <= 12; n++) {
            int result = 1 * n;
            System.out.println("1 times " + n + " = " + result);
        }
        for (int n = 1; n <= 12; n++) {
            int result = 2 * n;
            System.out.println("2 times " + n + " = " + result);
        }
        for (int n = 1; n <= 12; n++) {
            int result = 3 * n;
            System.out.println("3 times " + n + " = " + result);
        }
        for (int n = 1; n <= 12; n++) {
            int result = 4 * n;
            System.out.println("4 times " + n + " = " + result);
        }
        for (int n = 1; n <= 12; n++) {
            int result = 5 * n;
            System.out.println("5 times " + n + " = " + result);
        }
    }
    
}